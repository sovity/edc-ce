package de.sovity.edc.ce.api.common.model;

import com.fasterxml.jackson.annotation.JsonInclude;
import io.swagger.v3.oas.annotations.media.Schema;
import lombok.AllArgsConstructor;
import lombok.Data;
import lombok.NoArgsConstructor;
import lombok.NonNull;
import lombok.experimental.SuperBuilder;

@Data
@SuperBuilder
@NoArgsConstructor
@AllArgsConstructor
@JsonInclude(JsonInclude.Include.NON_NULL)
@Schema(description = "Sort a column of list pages by a given property")
public class SortByRequest<Field extends Enum<?>> {
    @NonNull
    @Schema(description = "Name of the column to sort for", requiredMode = Schema.RequiredMode.REQUIRED)
    private Field field;

    @Schema(description = "sort direction", requiredMode = Schema.RequiredMode.REQUIRED)
    private SortByDirection direction;
}
